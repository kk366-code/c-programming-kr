#include <stdio.h>

unsigned invert(unsigned x, int p, int n);

int main() {
    unsigned int x = 0b1010101010101010;  // 0xAAAA

    // p=11 (12ビット目から開始), n=5 (5ビット分を反転)
    int p = 11;
    int n = 5;

    unsigned int result = invert(x, p, n);

    printf("--- invert 関数の実行例 ---\n");
    printf("元の x (2進数下位): 1010 1010 1010 1010\n");
    printf("位置 p: %d, 幅 n: %d\n", p, n);
    printf("反転するビット: 10101 (位置 11〜7)\n");
    printf("---------------------------\n");
    printf("結果 x (16進数): 0x%04X\n", result);

    // 2進数の変化:
    // 元:  1010 1010 10[10101]0 1010
    // マスク: 0000 0000 00[11111]0 0000
    // 結果: 1010 1010 10[01010]0 1010
    //       0xAAAA -> 0xAA9A

    return 0;
}

/**
 * @brief x の p の位置から n ビット分を反転（トグル）する。
 * @param x ターゲットの数値
 * @param p 反転を開始する最上位ビットの位置 (右端を 0 とする)
 * @param n 反転するビットの数（幅）
 * @return 反転後の x の値
 */
unsigned invert(unsigned x, int p, int n) {
    // ----------------------------------------------------
    // 1. 反転用マスクの生成
    // ----------------------------------------------------
    // n_mask_ones: 下位 n ビットだけが 1 のマスクを生成
    //              例: n=5 なら 00...0011111
    unsigned int n_mask_ones = ~(~0U << n);

    // ----------------------------------------------------
    // 2. マスクを挿入位置に合わせてシフト
    // ----------------------------------------------------
    // invert_mask: n_mask_ones を p の位置まで左シフトする
    //              シフト量 = p + 1 - n (LSB の位置)
    unsigned int invert_mask = n_mask_ones << (p + 1 - n);

    // ----------------------------------------------------
    // 3. ターゲット値 x とマスクを XOR
    // ----------------------------------------------------
    // XORにより、マスクの 1 の位置にある x のビットは反転し、
    // マスクの 0 の位置にある x のビットはそのまま維持される。
    return x ^ invert_mask;
}
