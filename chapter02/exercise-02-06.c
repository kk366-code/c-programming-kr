#include <stdio.h>

unsigned setbits(unsigned x, int p, int n, unsigned y);

int main() {
    unsigned int x = 0xAAAA;  // 1010 1010 1010 1010
    unsigned int y = 0xBB;    // 0000 0000 1011 1011

    // p=11 (12ビット目から開始), n=5 (5ビット分を置き換え)
    int p = 11;
    int n = 5;

    unsigned int result = setbits(x, p, n, y);

    printf("--- setbits の実行例 ---\n");
    printf("元の x (16進): 0x%04X\n", x);
    printf("挿入する y (16進): 0x%04X\n", y);
    printf("位置 p: %d, 幅 n: %d\n", p, n);
    printf("---------------------------\n");
    printf("結果 (16進): 0x%04X\n", result);

    // 元の x の p=11からn=5のビット: 10101 -> 01010 (クリアされる)
    // y の右端 n=5 のビット: 11011 (抽出される)
    // 期待値: x の 10101 が 11011 に置き換わる
    // 期待結果 1010 1010 1010 1010 -> 1010 1011 1011 1010
    //            ^-----^      ^-----^
    //             元          結果
    // 1010 1011 1011 1010 -> 0xABBA

    // 1010 1010 1010 1010 (x)
    // 11011 (yのnビット)が 11〜7 の位置に挿入される
    // 1010 1011 1011 1010 (0xABBA)

    return 0;
}

/**
 * @brief x の p の位置から n ビット分を、y の右端 n ビットで置き換える。
 * @param x ターゲットの数値（置き換えられる側）
 * @param p 置き換えを開始する最上位ビットの位置 (右端を 0 とする)
 * @param n 置き換えるビットの数（幅）
 * @param y ソースの数値（右端 n ビットを抽出する側）
 * @return 置き換え後の x の値
 */
unsigned setbits(unsigned x, int p, int n, unsigned y) {
    // ----------------------------------------------------
    // 1. マスクの生成 (下位 n ビットが 1 のマスク)
    //    ~(~0U << n) は、下位 n ビットが 1、残りが 0 のマスクを生成
    // ----------------------------------------------------
    unsigned int n_mask = ~(~0U << n);

    // ----------------------------------------------------
    // 2. x の対象フィールドをクリア
    // ----------------------------------------------------
    // x_clear_mask: n_mask を p の位置にシフトし、NOT反転させたもの。
    //               x の対象フィールドだけが 0、残りが 1 のマスク。
    unsigned int x_clear_mask = ~(n_mask << (p + 1 - n));

    // x の対象フィールドを 0 にクリアする
    unsigned int x_cleared = x & x_clear_mask;

    // ----------------------------------------------------
    // 3. y のビットを挿入位置に合わせて準備
    // ----------------------------------------------------
    // y_data: y の下位 n ビットを抽出し、p の位置まで左シフトする
    unsigned int y_data = (y & n_mask) << (p + 1 - n);

    // ----------------------------------------------------
    // 4. クリアした x に y のデータを OR でセットする
    // ----------------------------------------------------
    return x_cleared | y_data;
}
